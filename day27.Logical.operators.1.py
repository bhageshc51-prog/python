# Operators

print("AND Operator")
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print("\nOR Operator")
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print("\nNOT Operator")
print(not True)
print(not False)
print(not(False))

