# Operators (Membership + Logical)

s = "Bhagesh"        # string variable storing "Bhagesh"
S2 = "Bhagesh.c"     # string variable storing "Bhagesh.c"

# Checks membership using AND operator
print(("a" in s) and ("s" in S2))

# Checks membership using OR operator
print(("e" in s) or ("h" in S2))